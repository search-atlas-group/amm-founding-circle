def _render_signatures(
    *,
    imports: Sequence[str],
    classes: Sequence[_ClassSummary],
    functions: Sequence[_FunctionSummary],
    assignments: Sequence[str],
) -> List[str]:
    lines: List[str] = []
    items: List[str] = []

    if imports:
        lines.append(f"imports ({len(imports)}):")
        for item in _shorten_list(imports, MAX_SKELETON_TOP_LEVEL_IMPORTS):
            lines.append(f"  - {item}")

    for cls in classes:
        items.append(f"{cls.signature} @ L{cls.lineno}")
        for method in cls.methods:
            items.append(f"{_strip_signature_prefix(method.signature)} @ L{method.lineno}")
    for func in functions:
        items.append(f"{func.signature} @ L{func.lineno}")
    for item in assignments:
        items.append(f"assign {item}")

    if items:
        lines.append(f"signatures ({len(items)}):")
        for item in _shorten_list(items, MAX_SIGNATURE_ITEMS):
            lines.append(f"  - {item}")

    return lines


def _render_digest_lines(*, path: str, line_count: int) -> List[str]:
    lines = [f"digest fallback for {Path(path).name or path}"]
    lines.append(f"lines: {line_count}")
    return lines


def _strip_signature_prefix(signature: str) -> str:
    if signature.startswith("async def "):
        return "async " + signature[len("async def ") :]
    if signature.startswith("def "):
        return signature[len("def ") :]
    return signature


def _render_import(node: ast.Import) -> str:
    parts = []
    for alias in node.names:
        if alias.asname:
            parts.append(f"{alias.name} as {alias.asname}")
        else:
            parts.append(alias.name)
    return f"import {', '.join(parts)}"


def _render_import_from(node: ast.ImportFrom) -> str:
    module = "." * node.level + (node.module or "")
    parts = []
    for alias in node.names:
        if alias.asname:
            parts.append(f"{alias.name} as {alias.asname}")
        else:
            parts.append(alias.name)
    return f"from {module} import {', '.join(parts)}"


def _build_class_signature(name: str, *, bases: Sequence[str], decorators: Sequence[str]) -> str:
    parts = []
    if decorators:
        parts.append(f"decorators={','.join(decorators)}")
    if bases:
        parts.append(f"bases={','.join(bases)}")
    if parts:
        return f"class {name} ({'; '.join(parts)})"
    return f"class {name}"


def _build_function_signature(
    node: ast.FunctionDef | ast.AsyncFunctionDef,
    *,
    owner: Optional[str],
) -> str:
    prefix = "async def" if isinstance(node, ast.AsyncFunctionDef) else "def"
    name = f"{owner}.{node.name}" if owner else node.name
    args = _render_arguments(node.args)
    signature = f"{prefix} {name}({args})"
    if node.returns is not None:
        signature += f" -> {_render_expr(node.returns)}"
    if node.decorator_list:
        decorators = _shorten_list([_render_expr(expr) for expr in node.decorator_list], MAX_DECORATORS)
        if decorators:
            signature += f" [decorators: {', '.join(decorators)}]"
    return signature


def _render_arguments(args: ast.arguments) -> str:
    rendered: List[str] = []
    positional = list(args.posonlyargs) + list(args.args)
    defaults = [None] * (len(positional) - len(args.defaults)) + list(args.defaults)

    for index, arg in enumerate(args.posonlyargs):
        rendered.append(_render_single_arg(arg, defaults[index]))
    if args.posonlyargs:
        rendered.append("/")

    offset = len(args.posonlyargs)
    for index, arg in enumerate(args.args):
        rendered.append(_render_single_arg(arg, defaults[offset + index]))

    if args.vararg is not None:
        rendered.append("*" + _render_arg_name(args.vararg))
    elif args.kwonlyargs:
        rendered.append("*")

    for arg, default in zip(args.kwonlyargs, args.kw_defaults):
        rendered.append(_render_single_arg(arg, default))

    if args.kwarg is not None:
        rendered.append("**" + _render_arg_name(args.kwarg))

    return ", ".join(part for part in rendered if part)


def _render_single_arg(arg: ast.arg, default: Optional[ast.expr]) -> str:
    rendered = _render_arg_name(arg)
    if arg.annotation is not None:
        rendered += f": {_render_expr(arg.annotation)}"
    if default is not None:
        rendered += f"={_render_expr(default)}"
    return rendered


def _render_arg_name(arg: ast.arg) -> str:
    return arg.arg


def _render_expr(node: ast.AST) -> str:
    if hasattr(ast, "unparse"):
        try:
            return ast.unparse(node)
        except Exception:
            pass
    return _fallback_render_expr(node)


def _fallback_render_expr(node: ast.AST) -> str:
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Attribute):
        return f"{_fallback_render_expr(node.value)}.{node.attr}"
    if isinstance(node, ast.Constant):
        return repr(node.value)
    if isinstance(node, ast.Subscript):
        return f"{_fallback_render_expr(node.value)}[{_fallback_render_expr(node.slice)}]"
    if isinstance(node, ast.Tuple):
        return ", ".join(_fallback_render_expr(item) for item in node.elts)
    if isinstance(node, ast.List):
        return "[" + ", ".join(_fallback_render_expr(item) for item in node.elts) + "]"
    if isinstance(node, ast.Dict):
        pairs = []
        for key, value in zip(node.keys, node.values):
            if key is None:
                pairs.append("**" + _fallback_render_expr(value))
            else:
                pairs.append(f"{_fallback_render_expr(key)}: {_fallback_render_expr(value)}")
        return "{" + ", ".join(pairs) + "}"
    if isinstance(node, ast.BinOp):
        return f"{_fallback_render_expr(node.left)} {_fallback_operator(node.op)} {_fallback_render_expr(node.right)}"
    if isinstance(node, ast.UnaryOp):
        return f"{_fallback_unary_operator(node.op)}{_fallback_render_expr(node.operand)}"
    if isinstance(node, ast.Call):
        args = [_fallback_render_expr(arg) for arg in node.args]
        kwargs = []
        for kw in node.keywords:
            if kw.arg is None:
                kwargs.append("**" + _fallback_render_expr(kw.value))
            else:
                kwargs.append(f"{kw.arg}={_fallback_render_expr(kw.value)}")
        all_args = args + kwargs
        return f"{_fallback_render_expr(node.func)}({', '.join(all_args)})"
    if isinstance(node, ast.JoinedStr):
        return "f" + repr("".join(_fallback_render_expr(value) for value in node.values))
    if isinstance(node, ast.FormattedValue):
        return "{" + _fallback_render_expr(node.value) + "}"
    return getattr(node, "id", getattr(node, "attr", node.__class__.__name__))


def _fallback_operator(op: ast.AST) -> str:
    mapping = {
        ast.Add: "+",
        ast.Sub: "-",
        ast.Mult: "*",
        ast.MatMult: "@",
        ast.Div: "/",
        ast.FloorDiv: "//",
        ast.Mod: "%",
        ast.Pow: "**",
        ast.LShift: "<<",
        ast.RShift: ">>",
        ast.BitOr: "|",
        ast.BitXor: "^",
        ast.BitAnd: "&",
        ast.And: "and",
        ast.Or: "or",
    }
    for cls, symbol in mapping.items():
        if isinstance(op, cls):
            return symbol
    return "?"


def _fallback_unary_operator(op: ast.AST) -> str:
    mapping = {
        ast.Not: "not ",
        ast.UAdd: "+",
        ast.USub: "-",
        ast.Invert: "~",
    }
    for cls, symbol in mapping.items():
        if isinstance(op, cls):
            return symbol
    return ""


MAX_MD_HEADINGS = 20
MAX_MD_HEADING_CHARS = 100
MAX_JSON_KEYS = 30
MAX_JSON_BYTES = 100 * 1024
MAX_NON_CODE_KEYS = 30

MAX_REPLACEMENT_CHARS_NON_CODE = {
    "outline": 800,
    "key_tree": 800,
    "section_list": 600,
}


def _summarize_non_code_source(
    source: str,
    *,
    file_path: str,
    suffix: str,
    offset: int = 0,
    limit: int = 0,
    file_tokens_est: Optional[int] = None,
    file_size_bytes: Optional[int] = None,
) -> StructureMapResult:
    if file_size_bytes is None:
        file_size_bytes = len(source.encode("utf-8", errors="ignore"))
    if file_tokens_est is None:
        file_tokens_est = estimate_tokens(source)

    language = LANGUAGE_LABELS.get(suffix, "unknown")
    line_count = source.count("\n") + (1 if source else 0)

    if offset != 0 or limit != 0:
        return _build_fallback_result(
            file_path=file_path, language=language, source=source,
            reason="partial_range_not_supported", confidence=0.12,
            parse_ok=False, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )

    if not source.strip():
        return _build_fallback_result(
            file_path=file_path, language=language, source=source,
            reason="empty_file", confidence=0.05,
            parse_ok=False, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )

    if suffix == ".md":
        return _summarize_markdown(source, file_path=file_path,
                                   line_count=line_count,
                                   file_tokens_est=file_tokens_est,
                                   file_size_bytes=file_size_bytes)
    if suffix == ".json":
        return _summarize_json(source, file_path=file_path,
                               line_count=line_count,
                               file_tokens_est=file_tokens_est,
                               file_size_bytes=file_size_bytes)
    if suffix in (".yaml", ".yml"):
        return _summarize_yaml(source, file_path=file_path,
                               line_count=line_count,
                               file_tokens_est=file_tokens_est,
                               file_size_bytes=file_size_bytes)
    if suffix == ".toml":
        return _summarize_toml(source, file_path=file_path,
                               line_count=line_count,
                               file_tokens_est=file_tokens_est,
                               file_size_bytes=file_size_bytes)

    return _build_fallback_result(
        file_path=file_path, language=language, source=source,
        reason="unsupported_language", confidence=0.10,
        parse_ok=False, generated_like=False,
        file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
    )


def _summarize_markdown(
    source: str, *, file_path: str, line_count: int,
    file_tokens_est: Optional[int], file_size_bytes: Optional[int],
) -> StructureMapResult:
    lines = source.splitlines()
    headings: list[str] = []
    for line in lines:
        stripped = line.strip()
        if stripped.startswith("#"):
            level = len(stripped) - len(stripped.lstrip("#"))
            title = stripped.lstrip("#").strip()
            if 1 <= level <= 4 and title:
                indent = "  " * (level - 1)
                headings.append(f"{indent}{title[:MAX_MD_HEADING_CHARS]}")
        if len(headings) >= MAX_MD_HEADINGS:
            break

    if not headings:
        return _build_fallback_result(
            file_path=file_path, language="markdown", source=source,
            reason="no_headings", confidence=0.15,
            parse_ok=False, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )

    name = Path(file_path).name
    rendered = f"Markdown outline for {name} ({line_count} lines):\n"
    rendered += "\n".join(headings)
    if len(headings) == MAX_MD_HEADINGS:
        rendered += f"\n... (truncated at {MAX_MD_HEADINGS} headings)"

    cap = MAX_REPLACEMENT_CHARS_NON_CODE["outline"]
    if len(rendered) > cap:
        rendered = rendered[:cap - 3] + "..."

    fp = _fingerprint(path=file_path, replacement_type="outline",
                       rendered=rendered, line_count=line_count,
                       file_size_bytes=file_size_bytes)
    return StructureMapResult(
        file_path=file_path, language="markdown",
        replacement_type="outline", replacement_text=rendered,
        replacement_tokens_est=estimate_tokens(rendered),
        confidence=0.90, fingerprint=fp, eligible=True,
        reason="ok", generated_like=False, parse_ok=True,
        line_count=line_count, file_tokens_est=file_tokens_est,
        file_size_bytes=file_size_bytes,
    )


def _json_type_hint(val: object) -> str:
    if isinstance(val, dict):
        return f"object({len(val)} keys)"
    if isinstance(val, list):
        return f"array({len(val)})"
    if isinstance(val, str):
        return f"string({len(val)}ch)" if len(val) > 40 else f'"{val}"'
    if isinstance(val, bool):
        return str(val).lower()
    if isinstance(val, (int, float)):
        return str(val)
    if val is None:
        return "null"
    return type(val).__name__


def _extract_json_keys(
    data: object, depth: int, max_depth: int, max_keys: int,
) -> list[str]:
    result: list[str] = []
    if depth > max_depth or len(result) >= max_keys:
        return result
    if isinstance(data, dict):
        for key in list(data.keys())[:max_keys - len(result)]:
            val = data[key]
            indent = "  " * depth
            hint = _json_type_hint(val)
            result.append(f"{indent}{key}: {hint}")
            if isinstance(val, dict) and depth < max_depth:
                result.extend(_extract_json_keys(val, depth + 1, max_depth, max_keys - len(result)))
            if len(result) >= max_keys:
                break
    elif isinstance(data, list) and data:
        result.append(f"{'  ' * depth}[{len(data)} items, first: {_json_type_hint(data[0])}]")
    return result[:max_keys]


def _summarize_json(
    source: str, *, file_path: str, line_count: int,
    file_tokens_est: Optional[int], file_size_bytes: Optional[int],
) -> StructureMapResult:
    raw_bytes = len(source.encode("utf-8", errors="replace"))
    if raw_bytes > MAX_JSON_BYTES:
        return _build_fallback_result(
            file_path=file_path, language="json", source=source,
            reason="too_large_to_parse", confidence=0.10,
            parse_ok=False, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )
    try:
        import json as _json
        data = _json.loads(source)
    except (ValueError, RecursionError):
        return _build_fallback_result(
            file_path=file_path, language="json", source=source,
            reason="parse_error", confidence=0.10,
            parse_ok=False, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )

    keys = _extract_json_keys(data, depth=0, max_depth=2, max_keys=MAX_JSON_KEYS)
    if not keys:
        return _build_fallback_result(
            file_path=file_path, language="json", source=source,
            reason="no_keys", confidence=0.15,
            parse_ok=True, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )

    name = Path(file_path).name
    rendered = f"JSON structure for {name}:\n" + "\n".join(keys)
    cap = MAX_REPLACEMENT_CHARS_NON_CODE["key_tree"]
    if len(rendered) > cap:
        rendered = rendered[:cap - 3] + "..."

    fp = _fingerprint(path=file_path, replacement_type="key_tree",
                       rendered=rendered, line_count=line_count,
                       file_size_bytes=file_size_bytes)
    return StructureMapResult(
        file_path=file_path, language="json",
        replacement_type="key_tree", replacement_text=rendered,
        replacement_tokens_est=estimate_tokens(rendered),
        confidence=0.92, fingerprint=fp, eligible=True,
        reason="ok", generated_like=False, parse_ok=True,
        line_count=line_count, file_tokens_est=file_tokens_est,
        file_size_bytes=file_size_bytes,
    )


def _summarize_yaml(
    source: str, *, file_path: str, line_count: int,
    file_tokens_est: Optional[int], file_size_bytes: Optional[int],
) -> StructureMapResult:
    lines = source.splitlines()
    indent_unit = 2
    for ln in lines:
        if ln and ln[0] == " ":
            spaces = len(ln) - len(ln.lstrip())
            if spaces > 0:
                indent_unit = spaces
                break

    keys: list[str] = []
    for line in lines:
        stripped = line.rstrip()
        if not stripped or stripped.lstrip().startswith("#"):
            continue
        indent = len(line) - len(line.lstrip())
        depth = indent // max(indent_unit, 1)
        if depth <= 2 and ":" in stripped:
            key_part = stripped.split(":")[0].strip().lstrip("- ")
            if key_part and not key_part.startswith("#"):
                keys.append(f"{'  ' * depth}{key_part}")
        if len(keys) >= MAX_NON_CODE_KEYS:
            break

    if not keys:
        return _build_fallback_result(
            file_path=file_path, language="yaml", source=source,
            reason="no_keys", confidence=0.15,
            parse_ok=False, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )

    name = Path(file_path).name
    rendered = f"YAML key tree for {name} ({line_count} lines):\n" + "\n".join(keys)
    cap = MAX_REPLACEMENT_CHARS_NON_CODE["key_tree"]
    if len(rendered) > cap:
        rendered = rendered[:cap - 3] + "..."

    fp = _fingerprint(path=file_path, replacement_type="key_tree",
                       rendered=rendered, line_count=line_count,
                       file_size_bytes=file_size_bytes)
    return StructureMapResult(
        file_path=file_path, language="yaml",
        replacement_type="key_tree", replacement_text=rendered,
        replacement_tokens_est=estimate_tokens(rendered),
        confidence=0.85, fingerprint=fp, eligible=True,
        reason="ok", generated_like=False, parse_ok=True,
        line_count=line_count, file_tokens_est=file_tokens_est,
        file_size_bytes=file_size_bytes,
    )


def _summarize_toml(
    source: str, *, file_path: str, line_count: int,
    file_tokens_est: Optional[int], file_size_bytes: Optional[int],
) -> StructureMapResult:
    sections: list[str] = []
    top_keys: list[str] = []
    for line in source.splitlines():
        stripped = line.strip()
        if stripped.startswith("[") and stripped.endswith("]"):
            sections.append(stripped)
        elif "=" in stripped and not stripped.startswith("#"):
            key = stripped.split("=")[0].strip()
            if key and len(top_keys) < 15:
                top_keys.append(key)

    if not sections and not top_keys:
        return _build_fallback_result(
            file_path=file_path, language="toml", source=source,
            reason="no_structure", confidence=0.15,
            parse_ok=False, generated_like=False,
            file_tokens_est=file_tokens_est, file_size_bytes=file_size_bytes,
        )

    name = Path(file_path).name
    parts = [f"TOML structure for {name} ({line_count} lines):"]
    if sections:
        parts.append("Sections:")
        parts.extend(f"  {s}" for s in sections[:15])
        if len(sections) > 15:
            parts.append(f"  ... ({len(sections) - 15} more)")
    if top_keys:
        parts.append("Top-level keys:")
        parts.extend(f"  {k}" for k in top_keys)

    rendered = "\n".join(parts)
    cap = MAX_REPLACEMENT_CHARS_NON_CODE["section_list"]
    if len(rendered) > cap:
        rendered = rendered[:cap - 3] + "..."

    fp = _fingerprint(path=file_path, replacement_type="section_list",
                       rendered=rendered, line_count=line_count,
                       file_size_bytes=file_size_bytes)
    return StructureMapResult(
        file_path=file_path, language="toml",
        replacement_type="section_list", replacement_text=rendered,
        replacement_tokens_est=estimate_tokens(rendered),
        confidence=0.88, fingerprint=fp, eligible=True,
        reason="ok", generated_like=False, parse_ok=True,
        line_count=line_count, file_tokens_est=file_tokens_est,
        file_size_bytes=file_size_bytes,
    )


def _build_fallback_result(
    *,
    file_path: str,
    language: str,
    source: str,
    reason: str,
    confidence: float,
    parse_ok: bool,
    generated_like: bool,
    file_tokens_est: Optional[int],
    file_size_bytes: Optional[int],
) -> StructureMapResult:
    line_count = source.count("\n") + (1 if source else 0)
    rendered = _render_digest(source=source, file_path=file_path, line_count=line_count)
    fingerprint = _fingerprint(
        path=file_path,
        replacement_type="digest",
        rendered=rendered,
        line_count=line_count,
        file_size_bytes=file_size_bytes,
    )
    return StructureMapResult(
        file_path=file_path,
        language=language,
        replacement_type="digest",
        replacement_text=rendered,
        replacement_tokens_est=estimate_tokens(rendered),
        confidence=max(0.05, min(1.0, confidence)),
        fingerprint=fingerprint,
        eligible=False,
        reason=reason,
        generated_like=generated_like,
        parse_ok=parse_ok,
        line_count=line_count,
        file_tokens_est=file_tokens_est,
        file_size_bytes=file_size_bytes,
    )


def _render_digest(*, source: str, file_path: str, line_count: int) -> str:
    lines = source.splitlines()
    if not lines:
        return f"digest fallback for {Path(file_path).name or file_path}\nlines: 0"

    non_empty = [line.rstrip() for line in lines if line.strip()]
    head = non_empty[:MAX_DIGEST_HEAD_LINES]
    tail = non_empty[-MAX_DIGEST_TAIL_LINES:] if len(non_empty) > MAX_DIGEST_HEAD_LINES + MAX_DIGEST_TAIL_LINES else []

    parts = [f"digest fallback for {Path(file_path).name or file_path}", f"lines: {line_count}"]
    if head:
        parts.append("head:")
        parts.extend(f"  - {line[:160]}" for line in head)
    if tail:
        parts.append("tail:")
        parts.extend(f"  - {line[:160]}" for line in tail)

    rendered = "\n".join(parts)
    cap = MAX_REPLACEMENT_CHARS["digest"]
    if len(rendered) <= cap:
        return rendered
    return rendered[: max(0, cap - 1)] + "…"


def _choose_replacement_type(
    *,
    file_tokens_est: int,
    line_count: int,
    class_count: int,
    function_count: int,
    assignment_count: int,
    method_count: int,
) -> str:
    total_symbols = class_count + function_count + assignment_count
    has_classes = class_count > 0
    has_methods = method_count > 0
    large_file = file_tokens_est >= 3500 or line_count >= 900 or total_symbols >= 18
    moderate_file = file_tokens_est >= 1500 or line_count >= 220 or total_symbols >= 8

    if has_classes and has_methods and moderate_file:
        return "skeleton"
    if large_file:
        return "signatures"
    if total_symbols <= 6 and line_count <= 180:
        return "top_level"
    if has_classes and moderate_file:
        return "skeleton"
    if total_symbols <= 12:
        return "top_level"
    return "signatures"


def _shrink_or_fallback(
    *,
    path: str,
    line_count: int,
    module_docstring: Optional[str],
    imports: Sequence[str],
    classes: Sequence[_ClassSummary],
    functions: Sequence[_FunctionSummary],
    assignments: Sequence[str],
    preferred: str,
) -> Tuple[str, str]:
    order = {
        "skeleton": ["skeleton", "top_level", "signatures", "digest"],
        "top_level": ["top_level", "signatures", "digest"],
        "signatures": ["signatures", "top_level", "digest"],
        "digest": ["digest"],
    }.get(preferred, ["digest"])

    for replacement_type in order:
        rendered = _render_candidate(
            replacement_type,
            path=path,
            line_count=line_count,
            module_docstring=module_docstring,
            imports=imports,
            classes=classes,
            functions=functions,
            assignments=assignments,
        )
        if len(rendered) <= MAX_REPLACEMENT_CHARS[replacement_type]:
            return rendered, replacement_type

    rendered = _render_digest_lines(path=path, line_count=line_count)
    return "\n".join(rendered), "digest"


def _score_confidence(
    *,
    replacement_type: str,
    line_count: int,
    file_tokens_est: int,
    class_count: int,
    function_count: int,
    method_count: int,
    rendered_len: int,
    parse_ok: bool,
    generated_like: bool,
) -> float:
    base = {
        "skeleton": 0.90,
        "top_level": 0.94,
        "signatures": 0.86,
        "digest": 0.28,
    }.get(replacement_type, 0.35)

    if parse_ok:
        base += 0.02
    if generated_like:
        base -= 0.20
    if file_tokens_est >= 4000:
        base -= 0.05
    if line_count >= 1200:
        base -= 0.05
    if class_count >= 3:
        base -= 0.03
    if method_count >= 8:
        base -= 0.04
    if function_count >= 12:
        base -= 0.03
    if rendered_len >= MAX_REPLACEMENT_CHARS.get(replacement_type, rendered_len):
        base -= 0.04

    return round(max(0.05, min(0.98, base)), 3)


def _fingerprint(
    *,
    path: str,
    replacement_type: str,
    rendered: str,
    line_count: int,
    file_size_bytes: Optional[int],
) -> str:
    payload = "\n".join(
        [
            "structure-map-v1",
            replacement_type,
            path,
            str(line_count),
            str(file_size_bytes or 0),
            rendered,
        ]
    )
    return hashlib.sha256(payload.encode("utf-8", errors="ignore")).hexdigest()


def _short_docstring(docstring: Optional[str]) -> str:
    if not docstring:
        return ""
    compact = " ".join(docstring.strip().split())
    if len(compact) <= MAX_DOCSTRING_CHARS:
        return compact
    return compact[: MAX_DOCSTRING_CHARS - 1] + "…"


def _shorten_list(items: Sequence, limit: int) -> List:
    if len(items) <= limit:
        return list(items)
    return list(items[:limit])


def _flatten_targets(node: ast.AST) -> List[str]:
    names: List[str] = []
    if isinstance(node, ast.Name):
        names.append(node.id)
    elif isinstance(node, (ast.Tuple, ast.List)):
        for child in node.elts:
            names.extend(_flatten_targets(child))
    elif isinstance(node, ast.Attribute):
        names.append(_render_expr(node))
    return names


def _is_public_symbol(name: str) -> bool:
    if name == "__all__":
        return True
    return not name.startswith("_")


