@dataclass(frozen=True)
class StructureMapResult:
    """Result of a conservative structure summary."""

    file_path: str
    language: str
    replacement_type: str
    replacement_text: str
    replacement_tokens_est: int
    confidence: float
    fingerprint: str
    eligible: bool
    reason: str
    generated_like: bool
    parse_ok: bool
    line_count: int
    file_tokens_est: Optional[int] = None
    file_size_bytes: Optional[int] = None

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass(frozen=True)
class _FunctionSummary:
    name: str
    signature: str
    lineno: int
    decorators: Tuple[str, ...]
    is_async: bool


@dataclass(frozen=True)
class _ClassSummary:
    name: str
    signature: str
    lineno: int
    decorators: Tuple[str, ...]
    methods: Tuple[_FunctionSummary, ...]


@dataclass(frozen=True)
class _JsTsSymbolSummary:
    kind: str
    name: str
    signature: str
    lineno: int
    exported: bool = False


def is_python_file(file_path: str | os.PathLike[str]) -> bool:
    """Return True when the path looks like a Python source file."""

    return Path(str(file_path)).suffix.lower() == PYTHON_SUFFIX


def is_js_ts_file(file_path: str | os.PathLike[str]) -> bool:
    """Return True when the path looks like a JS/TS-family source file."""

    return Path(str(file_path)).suffix.lower() in JS_TS_SUFFIXES


def detect_structure_language(file_path: str | os.PathLike[str]) -> str:
    """Return the structure-language label used by summaries."""

    suffix = Path(str(file_path)).suffix.lower()
    return LANGUAGE_LABELS.get(suffix, "unknown")


def is_structure_supported_file(file_path: str | os.PathLike[str]) -> bool:
    """Return True when this module may attempt a structure map."""

    if is_python_file(file_path) or is_js_ts_file(file_path):
        return True
    return Path(str(file_path)).suffix.lower() in NON_CODE_SUFFIXES


def estimate_tokens(text: str) -> int:
    """Estimate tokens via the shared calibrated estimator (U-F).

    Kept as a thin wrapper so existing call sites keep importing
    ``structure_map.estimate_tokens`` unchanged, while the calibration
    constant lives in one place (``token_estimate``). Falls back to the legacy
    4 chars/token ratio only if the shared module is unavailable.
    """

    if not text:
        return 0
    try:
        from token_estimate import estimate_tokens as _shared
        return _shared(text)
    except ImportError:
        return max(1, ceil(len(text) / 4))


def summarize_python_file(
    file_path: str | os.PathLike[str],
    *,
    content: Optional[str] = None,
    offset: int = 0,
    limit: int = 0,
    file_tokens_est: Optional[int] = None,
    file_size_bytes: Optional[int] = None,
) -> StructureMapResult:
    """Summarize a Python file from disk or supplied content.

    The summary is conservative: it only produces structure maps for whole-file
    rereads of Python sources and falls back to a small digest otherwise.
    """

    path = Path(str(file_path))
    source = content
    if source is None:
        try:
            source = path.read_text(encoding="utf-8", errors="replace")
        except OSError:
            return _build_fallback_result(
                file_path=str(path),
                language=detect_structure_language(path),
                source="",
                reason="unreadable",
                confidence=0.05,
                parse_ok=False,
                generated_like=False,
                file_tokens_est=file_tokens_est,
                file_size_bytes=file_size_bytes,
            )

    if file_size_bytes is None:
        file_size_bytes = len(source.encode("utf-8", errors="ignore"))
    if file_tokens_est is None:
        file_tokens_est = estimate_tokens(source)

    return summarize_python_source(
        source,
        file_path=str(path),
        offset=offset,
        limit=limit,
        file_tokens_est=file_tokens_est,
        file_size_bytes=file_size_bytes,
    )


