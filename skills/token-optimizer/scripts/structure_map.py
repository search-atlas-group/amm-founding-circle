#!/usr/bin/env python3
"""Conservative structure map extraction for code rereads.

This module summarizes a narrow set of code files into bounded structural views
that favor safety over completeness. It uses stdlib-only parsing and avoids
subprocess/network access. Unsupported, generated-looking, oversized, or
risky files degrade to a small digest instead of forcing a substitution.
"""

